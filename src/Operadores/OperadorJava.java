package Operadores;

/**
 * Esta clase es para el uso de conocimiento de los tipos de operadores de Java
 */
public class OperadorJava {
    public static void main(String[] args) {
        /*
        Operadores aritmeticos
            suma +
            resta -
            multi *
            div /
            resto %
         */
        int resultado,
                a = 7,
                b = 9;
        resultado = a + b;
        System.out.println("Suma = " + resultado);
        resultado = a - b;
        System.out.println("Resta = " + resultado);
        resultado = a * b;
        System.out.println("Multi = " + resultado);
        resultado = a / b;
        System.out.println("Div = " + resultado);
        resultado = a % b;
        System.out.println("MOD = " + resultado);

        /*
        Logicos, relacion, comparacion booleanos
        Mayor que           >
        Mayor o igual que   >=
        Menor que           <
        Menor o igual que   <=
        Igual               ==
        Distinto            !=

        and     &&
        Or      ||
        Not     !

        Aignacion
        igual       =
        incremento  +=
        decremen    -=
        miltplo     *=
        div         /=
        mod         %=

        Incremento
        ++
        Decremento
        --

        Concatenacion
        str + str
         */
    }
}
